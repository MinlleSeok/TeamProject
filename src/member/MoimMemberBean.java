package member;

public class MoimMemberBean {
	private int NUM;
	private int Moim_Num;
	private int UserNum;
	private int Level;
	
	
	public int getNUM() {
		return NUM;
	}
	public void setNUM(int nUM) {
		NUM = nUM;
	}
	public int getMoim_Num() {
		return Moim_Num;
	}
	public void setMoim_Num(int moim_Num) {
		Moim_Num = moim_Num;
	}
	public int getUserNum() {
		return UserNum;
	}
	public void setUserNum(int userNum) {
		UserNum = userNum;
	}
	public int getLevel() {
		return Level;
	}
	public void setLevel(int level) {
		Level = level;
	}
}
